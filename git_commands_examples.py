# cd -> change directory
#
# cd .\MAXMEDIA -> enters MAXMEDIA directory
#
# ls -> lists contents of current directory
#
# cat a.txt -> prints a.txt to console
#
# dir -Force ->this shows hidden files in current directory
#
# git clone "repo ssh url" -> this clones a git repo to your local machine
#
# git add . -> starts tracking all files
#
# git status -> tells status of files (ready to commit, not staged)
#
# git commit -m "Changed README.md; uploaded test file ceva.py; uploaded a list of git commands" -m "some description" -> commit example
#
# git remote add origin git@github.com:tudorobretin/test-repo2.git ->this sets the push destination
#
# git remote set-url <remote_name> <ssh_remote_url> -> this changes the ssh url
#
# git remote -v -> shows available destinations I think
#
# git checkout -b nameofnewbranch -> this creates a new branch
#
# git branch -> shows branches and current branch
#
# git push origin nameofnewbranch -> this pushes to github under the new branch
